#include "public.h"

/***********************************************************|
| 名称	:| 延时100us
| 晶振	:| 11.0592MHz
| 参数	:| 无
| 返回值	:| 无
/***********************************************************/
void delay100us()		//@11.0592MHz
{
	u8 i;

	_nop_();
	i = 43;
	while (--i);
}


/***********************************************************|
| 名称	:| 延时5ms
| 晶振	:| 11.0592MHz
| 参数	:| 无
| 返回值	:| 无
/***********************************************************/
void delay5ms()		//@11.0592MHz
{
	u8 i, j;

	i = 9;
	j = 244;
	do
	{
		while (--j);
	} while (--i);
}