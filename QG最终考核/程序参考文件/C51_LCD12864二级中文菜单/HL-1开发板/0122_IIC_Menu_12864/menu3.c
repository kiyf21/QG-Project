/***********************************************************|
| 名称	:| 显示帮助提示
| 参数	:| 无
| 返回值	:| 无
/***********************************************************/
void show_help()
{
	lcd12864_clear_area(6, 2, 32, 16, 1);		//黑色填充2个汉字字符位在第三行开始处
	lcd12864_clear_area(6, 3, 32, 16, 1);		//黑色填充2个汉字字符位在第四行开始处
	lcd12864_show_str(0, 2, "按ESC   →  退出");	//退出提示
	lcd12864_show_str(0, 3, "按ENTER →  保存");	//保存提示
}	


/***********************************************************|
| 名称	:| 一级菜单项目1下二级菜单项目1的设置项
| 参数	:| 无
| 返回值	:| 无
/***********************************************************/
void menu111()
{
	lcd12864_show_str(1, 0, "* 设置时间 *");		//显示第一行内容
	lcd12864_show_str(2, 1, "时  分  秒");		//设置项内容显示第二行
	showDec_byte(1, 1, cntH);					//在屏幕第2行第2个字符处显示小时
	showDec_byte(3, 1, cntM);					//在屏幕第2行第4个字符处显示分钟
	showDec_byte(5, 1, cntS);					//在屏幕第2行第6个字符处显示秒数
	show_help();								//显示退出/保存按键提示
	itemIndex = 3;								//将设置项索引设为3
	leftKey();									//调用左键程序左移1位，正好处于分位置
	lcd12864_write_cmd(0x0F);					//开启光标显示并闪烁
}


