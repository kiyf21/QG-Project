/***********************************************************|
| 名称	:| 刷新时分秒
| 参数	:| 无
| 返回值	:| 无
/***********************************************************/
void refresh_HMS()
{
	if (itemIndex == 1)
	{
		showDec_byte(1, 1, tmpH);			//每次小时改变后，在屏幕显示出来
		item_select();						//刷新光标位置在第二行第2个字符位
	}
	else if (itemIndex == 2)
	{
		showDec_byte(3, 1, tmpM);			//每次分钟改变后，在屏幕显示出来
		item_select();						//刷新光标位置在第二行第4个字符位
	}
	else if (itemIndex == 3)
	{
		showDec_byte(5, 1, tmpS);			//每次秒数改变后，在屏幕显示出来
		item_select();						//刷新光标位置在第二行第6个字符位
	}
}


/***********************************************************|
| 名称	:| 刷新年月日
| 参数	:| 无
| 返回值	:| 无
/***********************************************************/
void refresh_YMD()
{
	if (itemIndex == 1)
	{
		showDec_byte(1, 1, tmpY);			//每次年份改变后，在屏幕显示出来
		item_select();						//刷新光标位置在第二行第2个字符位
	}
	else if (itemIndex == 2)
	{
		showDec_byte(3, 1, tmpMh);			//每次月份改变后，在屏幕显示出来
		item_select();						//刷新光标位置在第二行第4个字符位
	}
	else if (itemIndex == 3)
	{
		showDec_byte(5, 1, tmpD);			//每天数改变后，在屏幕显示出来
		item_select();						//刷新光标位置在第二行第6个字符位
	}
}


/***********************************************************|
| 名称	:| 递增小时
| 参数	:| 无
| 返回值	:| 无
/***********************************************************/
void add_hour()
{	
	if (tmpH < 23)
	{
		tmpH++;
	}
	else
	{
		tmpH = 0;
	}
	refresh_HMS();
}


