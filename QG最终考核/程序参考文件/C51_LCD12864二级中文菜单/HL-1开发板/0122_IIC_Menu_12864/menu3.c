/***********************************************************|
| 名称	:| 进入项目设置界面
| 参数	:| 无
| 返回值	:| 无
/***********************************************************/
void enter_setItem()
{ 
	clear_arrow();								//先清除一级菜单显示的箭头，否则后面会残留显示
	lcd12864_clear_str(0, 0, 8);				//清除第1行字符
	clear_areaStr();							//清除屏幕第2-4行字符
	
	if (arrowPos1 == 1)
	{
		switch (arrowPos11)
		{
			case 1:
				menu111();						//显示一级菜单项目1下的二级菜单第一页
				tmpH = cntH;					//读取小时数据到缓冲区
				tmpM = cntM;					//读取分钟数据到缓冲区
				tmpS = cntS;					//读取秒数据到缓冲区
				break;
			case 2:
				menu112();						//显示一级菜单项目2下的二级菜单第一页
				tmpY  = year;					//读取年份数据到缓冲区
				tmpMh = month;					//读取月份数据到缓冲区
				tmpD  = day;					//读取日期据到缓冲区
				break;
			case 3: menu113(); break;			//显示一级菜单项目2下的二级菜单第一页
			default: break;
		}
	}
	
	mLayer = 3;									//将菜单层索引改为3	
}


/***********************************************************|
| 描述	:| 刷新设置项光标显示
| 参数	:| 无
| 返回值	:| 无
/***********************************************************/
void item_select()
{
	switch (itemIndex)
	{
		case 1: lcd12864_set_cursor(1, 1); break;		//设置光标位置在第三行第2个字符位
		case 2: lcd12864_set_cursor(3, 1); break;		//设置光标位置在第三行第4个字符位
		case 3: lcd12864_set_cursor(5, 1); break;		//设置光标位置在第三行第6个字符位
		default: break;
	}		
}


/***********************************************************|
| 名称	:| 设置项目左移
| 参数	:| u8 itemMax	-设置项最大个数
| 返回值	:| 无
/***********************************************************/
void item_lMove(u8 itemMax)
{
	if (itemIndex > 1)
	{
		itemIndex--;
	}
	else
	{ 
		itemIndex = itemMax;
	}
	item_select();				//选择设置项
}


/***********************************************************|
| 名称	:| 设置项目右移
| 参数	:| u8 itemMax	-设置项最大个数
| 返回值	:| 无
/***********************************************************/
void item_rMove(u8 itemMax)
{
	if (itemIndex < itemMax)
	{
		itemIndex++;
	}
	else
	{
		itemIndex = 1;
	}
	item_select();				//选择设置项
}


/***********************************************************|
| 名称	:| 左按键程序
| 参数	:| 无
| 返回值	:| 无
/***********************************************************/
void leftKey()
{
	if (itemIndex != 0)
	{
		switch (arrowPos1)
		{
			case 1:
				switch (arrowPos11)
				{
					case 1: item_lMove(3); break;		//设置时间界面左移
					case 2: item_lMove(3); break;		//设置日期界面左移
					default: break;
				}
				break;
			case 2:
				break;
			default:
				break;		
		}
	}
}


