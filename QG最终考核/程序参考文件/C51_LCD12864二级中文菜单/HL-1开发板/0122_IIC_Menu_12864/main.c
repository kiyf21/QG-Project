/***********************************************************|
| 名称	:| 将一个字节在屏幕指定位置以十六进制显示
| 参数1	:| u8 x		-光标在屏幕横向位置(0-7共8个汉字位置)
| 参数2	:| u8 y		-光标在屏幕纵向位置(0-3共4个汉字位置)
| 参数3	:| u8 byte	-要显示的一个字节数据
| 返回值	:| 无
/***********************************************************/
void showHex_byte(u8 x, u8 y, u8 byte)
{
	u8 buf[3];
	
	//时高4位转字符------------------------------------|
	if ((byte >> 4) <= 9)
	{
		buf[0] = (byte >> 4) + '0';				 		//高4位转字符0-9
	}
	else
	{
		buf[0] = ((byte >> 4) - 10) + 'A';				//高4位转字符A-F
	}
	
	//时低4位转字符------------------------------------|
	if ((byte & 0x0F) <= 9)
	{
		buf[1] = (byte & 0x0F) + '0';					//低4位转字符0-9
	}
	else
	{
		buf[1] = ((byte & 0x0F) - 10) + 'A';			//低4位转字符A-F
	}

	buf[2] = '\0';										//添加结束符
	lcd12864_show_str(x, y, buf);						//在屏幕指定位置显示该十六进制数
}


/***********************************************************|
| 描述	:| 时分秒转字符串在屏幕指定位置显示
| 参数1	:| u8 x		- 屏幕区域横向坐标(以16个像素点为一个单位)
| 参数2	:| u8 y		- 屏幕区域纵向坐标(以16个像素点为一个单位)
| 返回值	:| 无
/***********************************************************/
void time_str(u8 x, u8 y)
{
	u8 buf[9];
	
	//时高4位转字符------------------------------------
	if ((cntH >> 4) <= 9)
	{
		buf[0] = (cntH >> 4) + '0';				 		//高4位转字符0-9
	}
	else
	{
		buf[0] = ((cntH >> 4) - 10) + 'A';				//高4位转字符A-F
	}
	
	//时低4位转字符
	if ((cntH & 0x0F) <= 9)
	{
		buf[1] = (cntH & 0x0F) + '0';					//低4位转字符0-9
	}
	else
	{
		buf[1] = ((cntH & 0x0F) - 10) + 'A';			//低4位转字符A-F
	}
	
	buf[2] = ':';										//添加间隔符
	
	//分高4位转字符------------------------------------
	if ((cntM >> 4) <= 9)
	{
		buf[3] = (cntM >> 4) + '0';				 		//高4位转字符0-9
	}
	else
	{
		buf[3] = ((cntM >> 4) - 10) + 'A';				//高4位转字符A-F
	}
	
	//分低4位转字符
	if ((cntM & 0x0F) <= 9)
	{
		buf[4] = (cntM & 0x0F) + '0';					//低4位转字符0-9
	}
	else
	{
		buf[4] = ((cntM & 0x0F) - 10) + 'A';			//低4位转字符A-F
	}
	
	buf[5] = ':';										//添加间隔符
	
	//秒高4位转字符------------------------------------
	if ((cntS >> 4) <= 9)
	{
		buf[6] = (cntS >> 4) + '0';				 		//高4位转字符0-9
	}
	else
	{
		buf[6] = ((cntS >> 4) - 10) + 'A';				//高4位转字符A-F
	}
	
	//秒低4位转字符
	if ((cntS & 0x0F) <= 9)
	{
		buf[7] = (cntS & 0x0F) + '0';					//低4位转字符0-9
	}
	else
	{
		buf[7] = ((cntS & 0x0F) - 10) + 'A';			//低4位转字符A-F
	}
	buf[8] = '\0';										//添加结束符
	lcd12864_show_str(x, y, buf);		//在屏幕横向坐标x,纵向坐标y处显示字符串
}


