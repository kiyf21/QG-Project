void main()
{
	year = 21;
	month = 01;
	day = 07;
	//关HL-1开发板上的数码管显示
	P26 = 1;
	P0 = 0x00;
	P26 = 0;
	
	EA = 1;					//开总中断
	uart_init(9600);		//初始化uart,配置通信波特率9600
	timer0_init(1000);		//初始化定时器0，定时1ms
	lcd12864_init();		//初始化lcd12864液晶
	StartScreen();
	
	while (1)
	{
		if (flagUartRx)
		{
			pro_uartRx();					//处理串口数据
			flagUartRx = 0;
		}  
		
		if (flag200ms && (mLayer == 0))
		{
			flag200ms = 0;
			time_str(0, 2);					//在屏幕第3行第1个字符处显示时分秒		
			date_str();						//在屏幕第4行第1个字符处显示年月日
		}
		
		if (flag_autoExit)					//无操作计时到达自动退出标志位置1时
		{
			exit_set();						//退出设置界面
			flag_autoExit = 0;				//将自动退出标志位清0
		}
	}
}


