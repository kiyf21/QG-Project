/***********************************************************|
| 名称	:| 检测液晶是否忙，等待液晶准备好
| 参数	:| 无
| 返回值	:| 无
/***********************************************************/
void lcd12864_wait_ready()
{
	u8 sta;						//液晶数据口状态
	
	DB = 0xFF;					//这句一定要记住不能少啊，否则液晶不显示内容
	RS = 0;						//选择指令
	RW = 1;						//选择读取
	do {
		E = 1;					//打开液晶使能端
		sta = DB;				//读取液晶数据并保存
		E = 0;					//关闭液晶使能端
	} while (sta & 0x80);		//判断读取的液晶数据bit7位是否为1(忙)，循环检测直到为0(不忙)为止
}


/***********************************************************|
| 名称	:| 往液晶写入一个字节的指令
| 参数	:| u8 cmd	-待写指令
| 返回值	:| 无
/***********************************************************/
void lcd12864_write_cmd(u8 cmd)
{
	lcd12864_wait_ready();			//等待液晶准备好
	RS = 0;							//选择指令
	RW = 0;							//选择写入
	E  = 1;							//打开液晶使能端
	DB = cmd;						//将待写指令送到液晶数据口，等待写入
	E  = 0;							//关闭液晶使能端，指令在下降沿时写入液晶
	delay100us();					//延时
}


/***********************************************************|
| 名称	:| 往液晶写入一个字节的数据
| 参数	:| u8 cmd	-待写数据
| 返回值	:| 无
/***********************************************************/
void lcd12864_write_dat(u8 dat)
{
	lcd12864_wait_ready();			//等待液晶准备好
	RS = 1;							//选择数据
	RW = 0;							//选择写入
	E  = 1;							//打开液晶使能端
	DB = dat;						//将待写数据送到液晶数据口，等待写入
	E  = 0;							//关闭液晶使能端，数据在下降沿时写入液晶
	delay100us();					//延时
}


/***********************************************************|
| 名称	:| 设置光标在液晶屏幕的位置
| 参数1	:| u8 x	-光标在屏幕横向位置(以16个像素点为一个单位)
| 参数2	:| u8 y	-光标在屏幕纵向位置(以16个像素点为一个单位)
| 返回值	:| 无
/***********************************************************/
void lcd12864_set_cursor(u8 x, u8 y)
{
	u8 addr;								//光标RAM地址
	
	switch (y)
	{
		case 0: addr = 0x80 + x; break;		//设置光标在屏幕第一行x位置汉字位
		case 1: addr = 0x90 + x; break;		//设置光标在屏幕第二行x位置汉字位
		case 2: addr = 0x88 + x; break;		//设置光标在屏幕第三行x位置汉字位
		case 3: addr = 0x98 + x; break;		//设置光标在屏幕第四行x位置汉字位
		default: break;
	}
	lcd12864_write_cmd(addr);				//将光标RAM地址作为指令写入液晶	
	delay100us();							//延时
}


/***********************************************************|
| 名称	:| 在屏幕指定位置显示字符串
| 参数1	:| u8 x		- 光标在屏幕横向位置(以16个像素点为一个单位)
| 参数2	:| u8 y		- 光标在屏幕纵向位置(以16个像素点为一个单位)
| 参数3	:| u8 *str	- 字符串内存单元的指针
| 返回值	:| 无
/***********************************************************/
void lcd12864_show_str(u8 x, u8 y, u8 *str)
{
	//由起始DDRAM地址连续写入字符串
	lcd12864_write_cmd(0x30);				//启动DDRAM操作
	lcd12864_set_cursor(x, y);				//指定显示字符串的屏幕位置	
	while (*str != '\0')					//检测到写入的字符是结束符\0时，结束写入
	{
		lcd12864_write_dat(*str++);			//连续往液晶写入字符
	}
}


/***********************************************************|
| 描述	:| 在屏幕指定位置清除字符串
| 参数1	:| u8 x		- 光标在屏幕横向位置(以16个像素点为一个单位)
| 参数2	:| u8 y		- 光标在屏幕纵向位置(以16个像素点为一个单位)
| 参数3	:| u8 n		- 要清除的字符个数
| 返回值	:| 无
/***********************************************************/
void lcd12864_clear_str(u8 x, u8 y, u8 n)
{
	u8 i;
	
	//由起始DDRAM地址连续写入字符串
	lcd12864_write_cmd(0x30);				//启动DDRAM操作
	lcd12864_set_cursor(x, y);				//指定显示字符串的屏幕位置	
	i = n * 2;
	while (i--)								//检测到清除的字符个数为0时，结束清除
	{
		lcd12864_write_dat(' ');			//连续往液晶写入2个英文字符空格的空格
	}
}


