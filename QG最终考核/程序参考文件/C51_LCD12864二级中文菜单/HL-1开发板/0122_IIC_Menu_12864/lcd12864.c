/***********************************************************|
| 名称	:| lcd12864液晶初始化
| 参数	:| 无
| 返回值	:| 无
/***********************************************************/
void lcd12864_init()
{
	u8 x, y;
	
	//字符模式实始化
	lcd12864_write_cmd(0x30);				//基本指令集
	delay5ms();
	lcd12864_write_cmd(0x01);				//清零字符显示
	delay5ms();
	lcd12864_write_cmd(0x02);				//地址归位
	lcd12864_write_cmd(0x0C);				//显示开，光标不显示
	
	//图形模式初始化
	lcd12864_write_cmd(0x34);				//启动扩展指令，关闭图形显示
	for (y=0; y<32; y++)					//清零图形显示缓冲区
	{
		lcd12864_write_cmd(0x80 | y);
		lcd12864_write_cmd(0x80 | 0);
		for (x=0; x<32; x++)
		{
			lcd12864_write_dat(0x00);
		}
	}
//	lcd12864_write_cmd(0x36);				//开启图形显示(可以省略，因为绘图时会启动GDRAM操作)
}


/***********************************************************|
| 名称	:| 将一个字节的数据在液晶指定位置以十进制显示
| 参数1	:| u8 x		-光标在屏幕横向位置(0-7共8个汉字位置)
| 参数2	:| u8 y		-光标在屏幕纵向位置(0-3共4个汉字位置)
| 参数3	:| u8 byte	-要显示的一个字节数据
| 返回值	:| 无
/***********************************************************/
void showDec_byte(u8 x, u8 y, u8 dat)
{
	u8 buf[3];
	
	buf[0] = dat / 10 + '0';		//将数据对10取模+字符0放入数组第一个元素
	buf[1] = dat % 10 + '0';		//将数据对10取余+字符0存入数组第二个元素
	buf[2] = '\0';					//添加结束符
	lcd12864_show_str(x, y, buf);	//在屏幕x,y位置显示该十进制数
}


