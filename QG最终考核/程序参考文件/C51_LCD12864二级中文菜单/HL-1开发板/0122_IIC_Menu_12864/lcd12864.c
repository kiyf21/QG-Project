/***********************************************************|
| 名称	:| 在屏幕指定位置显示一幅图像
| 参数1	:| u8 x		- 屏幕区域横向坐标(以16个像素点为一个单位)
| 参数2	:| u8 y		- 屏幕区域纵向坐标(以16个像素点为一个单位)
| 参数3	:| u8 w		- 图像宽度(以1个像素点为一个单位)
| 参数4	:| u8 h		- 图像高度(以1个像素点为一个单位)
| 参数5	:| u8 *img	- 待显示图像的指针 
| 返回值	:| 无
| 备  注	:| x与w必须是16的整数倍,因CGRAM最小寻址单位为2字节,y与h可为0-63的任意值。
/***********************************************************/
void lcd12864_show_img(u8 x, u8 y, u8 w, u8 h, u8 *img)
{
	u16 i = 0;								//图像数据字节个数索引
	u8 xi, yi;								//图像数据横向和纵向循环索引变量
	u8 xt, yt;								//图像GDRAM水平位址(0-F)和垂直位址(0-31)
	
	w >>= 3;								//图像宽度/8,就是图像横向数据个数
	lcd12864_write_cmd(0x36);				//启动GDRAM操作	
	for (yi=0; yi<h; yi++)					//循环图像垂直位址(每次一个像素)
	{
		yt = y*16 + yi;						//计算图像垂直位址(绘图模式垂直位址以位为单位)
		xt = x;								//计算图像水平位址(绘图模式水平位址以字节为单位)
		if (yt >= 32)						//当图像垂直位址大于31时，应该到下半屏了
		{
			yt -= 32;						//图像垂直位址减掉32(绘图模式垂直位址范围0-31)
			xt += 8;						//图像水平位址+8(绘图模式水平位址0-F，其中8-F是下半屏)
		}
		lcd12864_write_cmd(0x80 | yt);		//往液晶写入待写图像垂直位址(这个要先写，否则显示错乱)
		lcd12864_write_cmd(0x80 | xt);		//往液晶写入待写图像水平位址
		for (xi=0; xi<w; xi++)
		{
			lcd12864_write_dat(img[i++]);	//连续写入一个字节的图像数据
		}
	}
}


/***********************************************************|
| 名称	:| 清除屏幕上的一块图形显示区域
| 参数1	:| u8 x		- 屏幕区域横向坐标(以16个像素点为一个单位)
| 参数2	:| u8 y		- 屏幕区域纵向坐标(以16个像素点为一个单位)
| 参数3	:| u8 w		- 图像宽度(以1个像素点为一个单位)
| 参数4	:| u8 h		- 图像高度(以1个像素点为一个单位)
| 参数5	:| bit wb	- 图像反白标记，1反白显示，0正常显示
| 返回值	:| 无
| 备  注	:| x与w必须是16的整数倍,因CGRAM最小寻址单位为2字节,y与h可为0-63的任意值。
/***********************************************************/
void lcd12864_clear_area(u8 x, u8 y, u8 w, u8 h, bit wb)
{
	u8 xi;									//图像数据横向循环索引变量
	u8 yi;									//图像像素纵向循环索引变量
	u8 xt;									//图像GDRAM水平位址(0-F)
	u8 yt;									//图像GDRAM垂直位址(0-31)
	
	w >>= 3;								//图像宽度/8,就是图像横向数据个数
	lcd12864_write_cmd(0x36);				//启动GDRAM操作
	for (yi=0; yi<h; yi++)					//循环图像垂直位址(每次一个像素)
	{
		yt = y*16 + yi;						//计算图像垂直位址(绘图模式垂直位址以位为单位)
		xt = x;								//计算图像水平位址(绘图模式水平位址以字节为单位)
		if (yt >= 32)						//当图像垂直位址大于31时，应该到下半屏了
		{
			yt -= 32;						//图像垂直位址减掉32(绘图模式垂直位址范围0-31)
			xt += 8;						//图像水平位址+8(绘图模式水平位址0-F，其中8-F是下半屏)
		}
		lcd12864_write_cmd(0x80 | yt);		//往液晶写入待写图像垂直位址(这个要先写，否则显示错乱)
		lcd12864_write_cmd(0x80 | xt);		//往液晶写入待写图像水平位址
		for (xi=0; xi<w; xi++)
		{
			if (wb)
			{
				lcd12864_write_dat(0xFF);	//向屏幕区域写入反白清屏数据
			}
			else
			{
				lcd12864_write_dat(0x00);	//向屏幕区域写入正常清屏数据
			}
		}
	}
}


