/***********************************************************|
| 名称	:| 进入一级菜单
| 参数	:| 无
| 返回值	:| 无
/***********************************************************/
void enter_menu1()
{	
	lcd12864_clear_str(0, 1, 8);				//清除第二行字符
	lcd12864_clear_str(0, 2, 8);				//清除第三行字符
	lcd12864_clear_str(0, 3, 8);				//清除第四行字符
	lcd12864_clear_area(0, 0, 128, 16, 1);		//反白第一行显示区域
	lcd12864_show_str(1, 0, "- 一级菜单 -");		//显示第一行内容
	
	if (mLayer == 1)							//如果之前是在一级菜单层
	{		
		page = 1;								//则设置一级菜单页码1
	}	
	menu1();									//显示一级菜单
}


/***********************************************************|
| 名称	:| ENTER键长按进入设置
| 参数	:| 无
| 返回值	:| 无
/***********************************************************/
void enter_set()
{
	if (mLayer == 0)
	{
		lcd12864_clear_area(1, 0, 96, 32, 0);		//清除屏幕上半屏的96*32图形区域
		mLayer = 1;									//置一级菜单层
		enter_menu1();								//进入一级菜单界面
		arrowPos1 = 1;								//设置一级菜单箭头位置为屏幕第二行
		arrow_position(arrowPos1);					//在屏幕第二行左边显示箭头
	}
	else
	{
		mLayer = 0;
		lcd12864_write_cmd(0x0C);					//关闭光标显示
		lcd12864_write_cmd(0x01);					//清零字符显示
		lcd12864_clear_area(6, 2, 32, 16, 0);		//清除2个汉字反白区域
		lcd12864_clear_area(6, 3, 32, 16, 0);		//清除2个汉字反白区域
		lcd12864_clear_area(0, 0, 128, 16, 0);		//清除第一行反白显示区域为正常显示
		StartScreen();
		flag_keyAc = 0;								//任意按键动作标志位清零
		//这里以后还有一行保存设置的语句
	}
			
}


/***********************************************************|
| 名称	:| ESC键长按退出设置
| 参数	:| 无
| 返回值	:| 无
/***********************************************************/
void exit_set()
{
	mLayer = 0;									//退出菜单
	lcd12864_write_cmd(0x0C);					//关闭光标显示
	lcd12864_write_cmd(0x01);					//清零字符显示
	lcd12864_clear_area(0, 0, 128, 16, 0);		//清除第一行反白显示区域为正常显示
	lcd12864_clear_area(6, 2, 32, 16, 0);		//清除2个汉字反白区域
	lcd12864_clear_area(6, 3, 32, 16, 0);		//清除2个汉字反白区域
	StartScreen();
	flag_keyAc = 0;								//任意按键动作标志位清零
}


/***********************************************************|
| 名称	:| ESC键单击返回上级菜单
| 参数	:| 无
| 返回值	:| 无
/***********************************************************/
void return_menu()
{
	if (mLayer == 2)
	{
		menu_page(arrowPos1);
		enter_menu1();								//与进入一级菜单一样处理
		arrow_position(arrowPos1);					//在屏幕第二行左边显示箭头
		mLayer = 1;			 						//置一级菜单层
	}
	else if (mLayer == 3)
	{
		lcd12864_write_cmd(0x0C);					//关闭光标显示
		lcd12864_clear_area(6, 2, 32, 16, 0);		//清除2个汉字反白区域
		lcd12864_clear_area(6, 3, 32, 16, 0);		//清除2个汉字反白区域
		clear_areaStr();							//清除屏幕2-4行字符
		menu2();
	}
}