void Bullet_MoveAll(void)
{
    static u8 i = 2;  // 静态变量：记住当前子弹位置

    if(delay_noblock(300))  // 200ms 移动一步，可自己改速度
    {
		if(i>2)
		{
			LCD12864_Show_ImageBullet(IMG_DATA_Empty,i-1);
		}
        LCD12864_Show_ImageBullet(Two_Bullet, i);
        i++;

        if(i >= 8)  // 子弹走到头
        {
            i = 2;   // 重置

        }
    }
	
}


